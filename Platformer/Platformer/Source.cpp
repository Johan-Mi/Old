constexpr int
LEVELWIDTH = 128,
LEVELHEIGHT = 17;

float fPlayerSpeed = 9.0f;
float fAnimationTimer = 0.0f;
unsigned int fAnimationCycle = 0;

class Example : public olc::PixelGameEngine {
public:
	Example() { sAppName = "Platformer"; }

public:
	bool OnUserCreate() override {
		SetPixelMode(olc::Pixel::MASK);

		std::clog << "---Sprites---\n";

		for(size_t i = 0; i < TILECOUNT; i++)
			sprTiles[i] = new olc::Sprite("..\\Textures\\Tiles\\tile_" + std::to_string(i + 1) + ".png");
		std::clog << "Tiles\n";

		for(size_t i = 0; i < PLAYERSPRITECOUNT; i++)
			sprPlayer[i] = new olc::Sprite("..\\Textures\\Sprites\\player_" + std::to_string(i) + ".png");
		std::clog << "Player\n";

		player.x = 3.0f;
		player.y = (float)LEVELHEIGHT - 5.0f;

		return true;
	}

	bool OnUserUpdate(float fElapsedTime) override {
		((fAnimationTimer += fElapsedTime) < 0.15f) || (fAnimationTimer = 0) || (++fAnimationCycle != 6) || (fAnimationCycle = 0);

		if(GetKey(olc::ESCAPE).bPressed)	return false;
		if(GetKey(olc::D).bHeld)			player.x += fPlayerSpeed * fElapsedTime;
		if(GetKey(olc::A).bHeld)			player.x -= fPlayerSpeed * fElapsedTime;
		if(GetKey(olc::S).bHeld)			player.y += fPlayerSpeed * fElapsedTime;
		if(GetKey(olc::W).bHeld)			player.y -= fPlayerSpeed * fElapsedTime;

		camera.x = player.x - (float)SCREENWIDTH / 2.0f;
		camera.y = player.y - (float)SCREENHEIGHT / 2.0f;

		if(camera.x < 0.0f)								camera.x = 0.0f;
		if(camera.y < 0.0f)								camera.y = 0.0f;
		if(camera.x > LEVELWIDTH - SCREENWIDTH - 1)		camera.x = (float)(LEVELWIDTH - SCREENWIDTH - 1);
		if(camera.y > LEVELHEIGHT - SCREENHEIGHT - 1)	camera.y = (float)(LEVELHEIGHT - SCREENHEIGHT - 1);
		
		Clear(olc::Pixel(0xfffc8868));
		
		int iDrawStartX = (int)floor(camera.x);
		int iDrawStartY = (int)floor(camera.y);
		for(unsigned int i = 0; i < SCREENHEIGHT + 1; i++) {
			for(unsigned int j = 0; j < SCREENWIDTH + 1; j++) {
				if(level[(iDrawStartY + i) * LEVELWIDTH + iDrawStartX + j] != 0) {
					unsigned int iTileNum = level[(iDrawStartY + i) * LEVELWIDTH + iDrawStartX + j];
					(iTileNum < 55) || (iTileNum += fAnimationCycle);
					DrawSprite((int32_t)(16 * (j - camera.x + iDrawStartX)), (int32_t)(16 * (i - camera.y + iDrawStartY)), sprTiles[iTileNum - 1]);
				}
			}
		}

		DrawSprite((int32_t)(16 * (player.x - camera.x)), (int32_t)(16 * (player.y - camera.y)), sprPlayer[0]);

		//DrawString(0, 0, std::to_string(player.x) + ", " + std::to_string(player.y), olc::WHITE);
		//DrawString(0, 8, std::to_string(camera.x) + ", " + std::to_string(camera.y), olc::WHITE);

		return true;
	}

	bool OnUserDestroy() override {
		for(auto i : sprTiles)	delete i;
		for(auto i : sprPlayer)	delete i;
		return true;
	}
};

int main() {
	Example demo;
	if(demo.Construct(SCREENWIDTH * 16, SCREENHEIGHT * 16, 3, 3))	demo.Start();
	return 0;
}